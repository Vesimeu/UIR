# Ввод скоростей автомобилей V1 и V2 (км/ч)
V1 = float(input("Введите скорость первого автомобиля V1 (1 <= V1 <= 100): "))
V2 = float(input("Введите скорость второго автомобиля V2 (1 <= V2 <= 100): "))

# Ввод начального расстояния между автомобилями S (км)
S = float(input("Введите начальное расстояние между автомобилями S (1 <= S <= 100): "))

# Ввод времени T (часы)
T = float(input("Введите время T (1 <= T <= 100): "))

# Проверка на допустимые значения
if (1 <= V1 <= 100) and (1 <= V2 <= 100) and (1 <= S <= 100) and (1 <= T <= 100):
    # Вычисление общего пути
    total_distance = T * (V1 + V2)

    # Вычисление расстояния между автомобилями через T часов
    distance_between = abs(S - total_distance)

    # Вывод расстояния с точностью до 4 знаков в дробной части
    print(f"Расстояние между автомобилями через {T} часов: {distance_between:.4f} км")
else:
    print("Введены недопустимые значения.")
