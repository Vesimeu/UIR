# Ввод координат двух точек
x1, y1 = map(int, input("Введите координаты первой точки (x1 y1): ").split())
x2, y2 = map(int, input("Введите координаты второй точки (x2 y2): ").split())

# Проверка на соответствие допустимому диапазону
if -100 <= x1 <= 100 and -100 <= y1 <= 100 and -100 <= x2 <= 100 and -100 <= y2 <= 100:
    # Вычисление расстояния
    distance = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5

    # Вывод результата с точностью до 5 знаков в дробной части
    print(f"Расстояние между точками: {distance:.5f}")
else:
    print("Координаты точек должны быть в диапазоне от -100 до 100.")
