# Ввод длины ребра куба
a = int(input("Введите длину ребра куба (1<=a<=100): "))

# Проверка на соответствие допустимому диапазону
if a < 1 or a > 100:
    print("Длина ребра куба должна быть в диапазоне от 1 до 100.")
else:
    # Вычисление объема и площади поверхности куба
    volume = a ** 3
    surface_area = 6 * a ** 2

    # Вывод результатов с точностью до 4 знаков в дробной части
    print(f"Объем куба: {volume:.4f}")
    print(f"Площадь поверхности куба: {surface_area:.4f}")
